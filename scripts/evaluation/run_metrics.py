#!/usr/bin/env python3
"""Run all automated quality metrics on translated sentences.

Metrics computed per model:
  1. COMET-Kiwi      — reference-free neural (Unbabel/wmt22-cometkiwi-da)
  2. COMET-22        — reference-based neural (Unbabel/wmt22-comet-da)
  3. MetricX-24      — reference-based neural MQM error (google/metricx-24)
  4. BERTScore       — reference-based neural (bert-base-multilingual-cased)
  5. chrF++          — character+word n-gram overlap (sacrebleu, char_order=4)
  6. spBLEU          — SentencePiece-tokenized BLEU (TranslateGemma tokenizer)
  7. Morph-BLEU      — Turkish suffix-stripped BLEU (agglutinative morphology)

References: gold standard translations generated by Gemini 3.1 Pro

Output:
  - data/output/model_selection/metrics.json          — per-model scores
  - data/output/model_selection/human_eval_data.json  — for index.html
"""
import json, os, sys, time
from pathlib import Path

# Monkey patch to fix transformers 5.x compatibility shims with COMET
try:
    from transformers import XLMRobertaTokenizer, XLMRobertaTokenizerFast
    def _build_inputs_with_special_tokens(self, token_ids_0, token_ids_1=None):
        if token_ids_1 is None:
            return [self.bos_token_id] + token_ids_0 + [self.eos_token_id]
        return [self.bos_token_id] + token_ids_0 + [self.eos_token_id] + [self.bos_token_id] + token_ids_1 + [self.eos_token_id]
    XLMRobertaTokenizer.build_inputs_with_special_tokens = _build_inputs_with_special_tokens
    XLMRobertaTokenizerFast.build_inputs_with_special_tokens = _build_inputs_with_special_tokens

    import comet.encoders.xlmr
    def _forward_patched(self, input_ids, attention_mask, **kwargs):
        model_output = self.model(
            input_ids=input_ids,
            attention_mask=attention_mask,
            output_hidden_states=True,
            return_dict=True,
        )
        return {
            "sentemb": model_output.last_hidden_state[:, 0, :],
            "wordemb": model_output.last_hidden_state,
            "all_layers": model_output.hidden_states,
            "attention_mask": attention_mask,
        }
    comet.encoders.xlmr.XLMREncoder.forward = _forward_patched
except Exception as e:
    print(f"DEBUG: Monkey patch failed with: {e}")

ROOT = Path(__file__).resolve().parent.parent.parent
IDIR = ROOT / "data" / "output" / "model_selection"
INFILE = IDIR / "translations.json"
REF_FILE = IDIR / "references.jsonl"
METRICS_FILE = IDIR / "metrics.json"
EVAL_FILE = IDIR / "human_eval_data.json"

os.environ.setdefault("TOKENIZERS_PARALLELISM", "false")
os.environ.pop("HF_HUB_ENABLE_HF_TRANSFER", None)
os.chdir(str(ROOT))
sys.path.insert(0, str(ROOT))


def load_references():
    """Load references as a dict keyed by source_text for proper alignment."""
    if REF_FILE.exists():
        ref_map = {}
        with open(REF_FILE, encoding="utf-8") as f:
            for line in f:
                try:
                    obj = json.loads(line.strip())
                    src = obj.get("source_text", "").strip()
                    r = obj.get("reference_translation", "")
                    if src and r:
                        ref_map[src] = r
                except Exception: pass
        if ref_map:
            print(f"References: {len(ref_map)} from {REF_FILE}")
            return ref_map
    return {}


_sp_tokenizer = None

def compute_spbleu(hyps, refs):
    """SentencePiece-tokenized BLEU using the TranslateGemma 4B tokenizer."""
    global _sp_tokenizer
    import sacrebleu
    if _sp_tokenizer is None:
        from transformers import AutoTokenizer
        try:
            _sp_tokenizer = AutoTokenizer.from_pretrained("google/translategemma-4b-it")
        except Exception:
            return {"score": None, "error": "tokenizer not available"}
    sp_tok = _sp_tokenizer
    hyp_tok = [" ".join(sp_tok.convert_ids_to_tokens(sp_tok.encode(h, add_special_tokens=False))) for h in hyps]
    ref_tok = [[" ".join(sp_tok.convert_ids_to_tokens(sp_tok.encode(r, add_special_tokens=False))) for r in refs]]
    result = sacrebleu.corpus_bleu(hyp_tok, ref_tok, tokenize="none")
    return {"score": round(result.score, 1)}


def compute_morph_bleu(hyps, refs):
    """Turkish morphology-aware BLEU — strips agglutinative suffixes before scoring."""
    import sacrebleu
    TR_SUFFIXES = [
        "ler", "lar", "de", "da", "den", "dan", "e", "a", "i", "u", "ü", "ı",
        "in", "un", "ün", "ın", "nin", "nun", "nün", "nın",
        "dir", "dır", "dur", "dür", "tir", "tır", "tur", "tür",
        "li", "lı", "lu", "lü", "siz", "sız", "suz", "süz",
        "ce", "ca", "çe", "ça", "ki", "kü", "ci", "cı", "cu", "cü",
        "miş", "mış", "muş", "müş", "di", "dı", "du", "dü",
    ]
    TR_SUFFIXES.sort(key=len, reverse=True)

    def strip_suffixes(word):
        lower = word.lower()
        for s in TR_SUFFIXES:
            if lower.endswith(s) and len(lower) > len(s) + 2:
                return lower[:-len(s)]
        return lower

    hyp_stripped = [" ".join(strip_suffixes(w) for w in h.split()) for h in hyps]
    ref_stripped = [[" ".join(strip_suffixes(w) for w in r.split()) for r in refs]]
    result = sacrebleu.corpus_bleu(hyp_stripped, ref_stripped, tokenize="intl")
    return {"score": round(result.score, 1)}


def main():
    IDIR.mkdir(parents=True, exist_ok=True)

    with open(INFILE) as f:
        translations = json.load(f)

    model_ids = sorted({mid for e in translations
                       for mid in e["models"].keys()
                       if "NOT AVAILABLE" not in e["models"][mid].get("text", "")})
    sources = [e["source_text"] for e in translations]
    hyps = {mid: [e["models"][mid]["text"] for e in translations]
            for mid in model_ids}
    ref_map = load_references()

    # Align references to translations by source text, not positional index
    refs = []
    for src in sources:
        refs.append(ref_map.get(src.strip(), ""))
    has_refs = ref_map and all(r for r in refs)
    n_matched = sum(1 for r in refs if r)
    n_missing = len(refs) - n_matched

    print(f"Sources: {len(sources)}  Models: {len(model_ids)}  Refs matched: {n_matched}  Missing: {n_missing}")
    if n_missing > 0:
        print(f"  WARNING: {n_missing} source(s) have no matching reference — reference-based metrics will use only matched pairs")
    print()

    from benchmark.quality.metrics_comet import compute_comet_kiwi, compute_comet

    metrics_list = []
    for mid in model_ids:
        h = hyps[mid]
        print(f"--- {mid} ({len(h)} translations) ---")
        result = {"model_id": mid, "metrics": {}}

        # 1. COMET-Kiwi — reference-free, always available
        try:
            kiwi = compute_comet_kiwi(sources, h)
            result["metrics"]["comet_kiwi"] = kiwi.get("system_score")
            print(f"  COMET-Kiwi:    {kiwi.get('system_score', 'N/A')}")
        except Exception as e:
            print(f"  COMET-Kiwi:    ERROR — {e}")

        # Build aligned triplets — only include pairs with a valid reference
        matched_src, matched_hyp, matched_ref = [], [], []
        for s, hyp, r in zip(sources, h, refs):
            if r:  # non-empty reference exists for this source
                matched_src.append(s)
                matched_hyp.append(hyp)
                matched_ref.append(r)

        if not matched_ref:
            print("  (reference-based metrics skipped — no matching references)")
            metrics_list.append(result)
            continue

        print(f"  Using {len(matched_ref)}/{len(sources)} matched source-reference pairs")

        # 2. COMET-22 — reference-based
        try:
            comet = compute_comet(matched_src, matched_hyp, matched_ref)
            result["metrics"]["comet"] = comet.get("system_score")
            print(f"  COMET-22:      {comet.get('system_score', 'N/A')}")
        except Exception as e:
            print(f"  COMET-22:      ERROR — {e}")

        # 3. MetricX-24 — reference-based neural MQM error (lower=better)
        try:
            from benchmark.quality.metrics_metricx import compute_metricx
            clean_src = [str(s) for s in matched_src]
            clean_hyp = [str(hyp) for hyp in matched_hyp]
            clean_ref = [str(r) for r in matched_ref]
            mx = compute_metricx(clean_src, clean_hyp, clean_ref)
            result["metrics"]["metricx_24"] = mx.get("system_score")
            print(f"  MetricX-24:    {mx.get('system_score', 'N/A')}")
        except Exception as e:
            print(f"  MetricX-24:    ERROR — {e}")

        # 4. BERTScore — reference-based
        try:
            from benchmark.quality.metrics_bertscore import compute_bertscore
            b = compute_bertscore(matched_ref, matched_hyp)
            result["metrics"]["bertscore"] = b.get("system_score")
            print(f"  BERTScore:     {b.get('system_score', 'N/A')}")
        except Exception as e:
            print(f"  BERTScore:     ERROR — {e}")

        # 5. chrF++ — character+word n-gram
        try:
            from benchmark.quality.metrics_chrf import compute_chrf
            c = compute_chrf(matched_hyp, [matched_ref])
            result["metrics"]["chrf"] = c.get("score")
            print(f"  chrF++:        {c.get('score', 'N/A')}")
        except Exception as e:
            print(f"  chrF++:        ERROR — {e}")

        # 6. spBLEU — SentencePiece-tokenized BLEU
        try:
            sp = compute_spbleu(matched_hyp, matched_ref)
            result["metrics"]["spbleu"] = sp.get("score")
            print(f"  spBLEU:        {sp.get('score', 'N/A')}")
        except Exception as e:
            print(f"  spBLEU:        ERROR — {e}")

        # 7. Morph-BLEU — Turkish suffix-stripped BLEU
        try:
            mb = compute_morph_bleu(matched_hyp, matched_ref)
            result["metrics"]["morph_bleu"] = mb.get("score")
            print(f"  Morph-BLEU:    {mb.get('score', 'N/A')}")
        except Exception as e:
            print(f"  Morph-BLEU:    ERROR — {e}")

        metrics_list.append(result)

    with open(METRICS_FILE, "w", encoding="utf-8") as f:
        json.dump(metrics_list, f, indent=2, ensure_ascii=False)
    print(f"\nMetrics → {METRICS_FILE}")

    # ── Human evaluation data ─────────────────────────────────
    import random
    rng = random.Random(42)

    eval_data = {
        "model_ids": model_ids,
        "models_per_source": len(model_ids),
        "sources": [],
    }

    for entry in translations:
        mids = list(model_ids)
        rng.shuffle(mids)
        labels = [chr(ord("A") + j) for j in range(len(mids))]
        label_map = {lbl: mid for lbl, mid in zip(labels, mids)}
        eval_data["sources"].append({
            "id": entry["source_id"],
            "text": entry["source_text"],
            "translations": [
                {"model_label": lbl, "text": entry["models"].get(mid, {}).get("text", "N/A")}
                for lbl, mid in label_map.items()
            ],
        })

    with open(EVAL_FILE, "w", encoding="utf-8") as f:
        json.dump(eval_data, f, indent=2, ensure_ascii=False)
    print(f"Eval data → {EVAL_FILE}")
    print(f"  {len(eval_data['sources'])} sources × {len(model_ids)} models")
    print(f"\nOpen scripts/evaluation/index.html → rate 1-10 → download")
    print(f"Then: python3 scripts/evaluation/tune_weights.py")


if __name__ == "__main__":
    main()
